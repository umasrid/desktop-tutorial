/*write a programme to perform addition and subtraction of any 
two integers and two real numbers*/

#include<stdio.h>
int main()
{
	int m1,m2,add,sub;
	float n1,n2,sum,diff;
	
	printf("enter any two integers");
	scanf("%d%d",&m1,&m2);
	printf("addition of two integers are %d\n",add);
	printf("subraction of two integers are %d\n",sub);
	
	printf("enter any two real numbers");
	scanf("%f%f",&n1,&n2);
	printf("addition of two integers are %f\n",sum);
	printf("subraction of two integers are %f\n",diff);
	return 0;
 } 
